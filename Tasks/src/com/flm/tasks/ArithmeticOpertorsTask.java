package com.flm.tasks;

public class ArithmeticOpertorsTask {
	public static void main(String[] args) {
		int a =10;
		int b = 20;
		float c = 5.0f;
		int sum = a+b;
		System.out.println(a + " + " + b + " -> " + sum);
		float mul = a*c;
		System.out.println(a + " * " + c + " -> " +  mul);
	}

}
